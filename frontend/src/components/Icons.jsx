/**
 * Returns the appropriate icon component for a character ID
 */
export function CharacterIcon({ characterId, size = 24, className = '' }) {
  switch (characterId) {
    case 'gentleman': return <IconGlasses size={size} className={className} />;
    case 'rhymer': return <IconMic size={size} className={className} />;
    case 'rhyme_emperor': return <IconTrophy size={size} className={className} />;
    case 'gojo': return <IconInfinity size={size} className={className} />;
    case 'hiroyuki': return <IconSmile size={size} className={className} />;
    case 'nemu': return <IconBook size={size} className={className} />;
    case 'cold': return <IconSnowflake size={size} className={className} />;
    case 'hayato': return <IconChart size={size} className={className} />;
    case 'zetto': return <IconEye size={size} className={className} />;
    case 'joker': return <IconDice size={size} className={className} />;
    default: return <IconBot size={size} className={className} />;
  }
}

/**
 * Returns the appropriate icon component for a mode ID
 */
export function ModeIcon({ modeId, size = 24, className = '' }) {
  switch (modeId) {
    case 'debate': return <IconTarget size={size} className={className} />;
    case 'rap': return <IconMic size={size} className={className} />;
    case 'persuade': return <IconHome size={size} className={className} />;
    default: return <IconTarget size={size} className={className} />;
  }
}
